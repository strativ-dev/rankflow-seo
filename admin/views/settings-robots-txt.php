<script>
    jQuery(document).ready(function ($) {
        // Copy to clipboard with fallback.
        $('#copy-robots-txt').on('click', function () {
            var content = $('#robots-preview-content').text();
            var $btn = $('#copy-robots-txt');

            // Try modern clipboard API first.
            if (navigator.clipboard && window.isSecureContext) {
                navigator.clipboard.writeText(content).then(function () {
                    showCopySuccess($btn);
                }).catch(function () {
                    fallbackCopy(content, $btn);
                });
            } else {
                fallbackCopy(content, $btn);
            }
        });

        // Fallback copy method.
        function fallbackCopy(text, $btn) {
            var $temp = $('<textarea>');
            $temp.css({
                position: 'fixed',
                left: '-9999px',
                top: '0'
            });
            $temp.val(text);
            $('body').append($temp);
            $temp.select();

            try {
                document.execCommand('copy');
                showCopySuccess($btn);
            } catch (err) {
                alert('<?php esc_html_e('Failed to copy. Please select and copy manually.', 'rankflow-seo'); ?>');
            }

            $temp.remove();
        }

        // Show success message.
        function showCopySuccess($btn) {
            var originalText = $btn.html();
            $btn.html('<span class="dashicons dashicons-yes"></span> <?php esc_html_e('Copied!', 'rankflow-seo'); ?>');
            setTimeout(function () {
                $btn.html(originalText);
            }, 2000);
        }

        // Refresh preview.
        $('#rankflow-seo-preview-robots').on('click', function () {
            var $btn = $(this);
            $btn.prop('disabled', true);

            $.ajax({
                url: ajaxurl,
                type: 'POST',
                data: {
                    action: 'rankflow_seo_preview_robots',
                    nonce: '<?php echo esc_js(wp_create_nonce('rankflow_seo_robots_nonce')); ?>'
                },
                success: function (response) {
                    if (response.success) {
                        $('#robots-preview-content').text(response.data.content);
                    }
                    $btn.prop('disabled', false);
                },
                error: function () {
                    $btn.prop('disabled', false);
                }
            });
        });

        // Reset to defaults.
        $('#rankflow-seo-reset-robots').on('click', function () {
            if (!confirm('<?php esc_html_e('Are you sure you want to reset all robots.txt settings to defaults?', 'rankflow-seo'); ?>')) {
                return;
            }

            var $btn = $(this);
            $btn.prop('disabled', true);

            $.ajax({
                url: ajaxurl,
                type: 'POST',
                data: {
                    action: 'rankflow_seo_reset_robots',
                    nonce: '<?php echo esc_js(wp_create_nonce('rankflow_seo_robots_nonce')); ?>'
                },
                success: function (response) {
                    if (response.success) {
                        location.reload();
                    }
                    $btn.prop('disabled', false);
                },
                error: function () {
                    $btn.prop('disabled', false);
                }
            });
        });
    });
</script>

